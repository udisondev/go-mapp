package gen

import (
	"fmt"
	"log"

	"github.com/dave/jennifer/jen"
	. "github.com/dave/jennifer/jen"
	"github.com/udisondev/go-mapp/mapp"
)

const charset = "abcdefghijklmnopqrstuvwxyz"

type mapperFunc struct {
	isRoot             bool
	file               *File
	generatedFn        *Statement
	mapper             mapp.Mapper
	source             mapp.Field
	target             mapp.Field
	submappers         map[string]string
	enmMappers         map[string]map[string]mapp.EnumMapper
	fieldMapGenerators map[mapp.TypeFamily]map[mapp.TypeFamily]func(bl mapperBlock, s, t mapp.Field, opts ...genOpts) error
}

type mapperBlock struct {
	*Group
	mapperFunc
}

type genParams struct {
	srcIsPointer, targetIsPointer bool
	withErr                       bool
	withPanic                     bool
	panicMsg                      string
	isTargetStrct                 bool
	strPath                       string
	strType                       string
}

type genOpts func(genParams) genParams

func withSrcIsPointer() genOpts {
	return func(gp genParams) genParams {
		gp.srcIsPointer = true
		return gp
	}
}

func withErr(b bool) genOpts {
	return func(gp genParams) genParams {
		gp.withErr = b
		return gp
	}
}

func isTargetStrct(b bool) genOpts {
	return func(gp genParams) genParams {
		gp.isTargetStrct = b
		return gp
	}
}

func withPanic(msg string) genOpts {
	return func(gp genParams) genParams {
		gp.withPanic = true
		gp.panicMsg = msg
		return gp
	}
}

func Generate(mf mapp.File) {
	f := NewFile("main")

	f.Comment("Code generated by go-mapp. DO NOT EDIT.")

	submappers := make(map[string]string)
	fieldMapGenerators := map[mapp.TypeFamily]map[mapp.TypeFamily]func(bl mapperBlock, s, t mapp.Field, opts ...genOpts) error{
		mapp.FieldTypeBasic: {
			mapp.FieldTypeBasic:   basicToBasic,
			mapp.FieldTypePointer: basicToPointer},
		mapp.FieldTypePointer: {
			mapp.FieldTypeBasic:   pointerToBasic,
			mapp.FieldTypeStruct:  pointerToStruct,
			mapp.FieldTypePointer: pointerToPointer,
		},
		mapp.FieldTypeStruct: {
			mapp.FieldTypeStruct:  structToStruct,
			mapp.FieldTypePointer: structToPointer,
		},
		mapp.FieldTypeSlice: {mapp.FieldTypeSlice: sliceToSlice},
		mapp.FieldTypeNamed: {mapp.FieldTypeNamed: namedToNamed},
	}

	enumMappers := make(map[string]map[string]mapp.EnumMapper)

	for _, em := range mf.EnumsMappers() {
		if tm, ok := enumMappers[enumHash(em.Source())]; ok {
			if _, ok := tm[enumHash(em.Target())]; ok {
				continue
			}
		}
		generateEnumMapper(f, em, func(s, t string, em mapp.EnumMapper) { enumMappers[s] = map[string]mapp.EnumMapper{t: em} })
	}
	for _, m := range mf.Mappers() {
		mfn := mapperFunc{
			isRoot:             true,
			file:               f,
			generatedFn:        f.Func().Id(m.Name()),
			mapper:             m,
			submappers:         submappers,
			enmMappers:         enumMappers,
			fieldMapGenerators: fieldMapGenerators,
		}
		f.Line()
		mfn.generateSignature()
		err := mfn.generateBlock()
		if err != nil {
			log.Fatalf("Error generate mapper '%s': %v", m.Name(), err)
		}
	}

	err := f.Save("./mapper_impl.go")
	if err != nil {
		log.Fatalf("failed to save file: %v", err)
	}
}

func generateEnumMapper(f *File, em mapp.EnumMapper, include func(s, t string, em mapp.EnumMapper)) {
	sourcePath := em.Source().Path()
	targetPath := em.Target().Path()
	_, sourceT := em.Source().Type()
	_, targetT := em.Target().Type()

	include(enumHash(em.Source()), enumHash(em.Target()), em)

	msg, withErr := em.Errormsg()
	sign := f.Func().Id(em.Name()).Params(Id(em.Source().Name()).Qual(sourcePath, sourceT))
	if withErr {
		sign.Params(Qual(targetPath, targetT), Id("error"))
	} else {
		sign.Qual(targetPath, targetT)
	}
	sign.
		BlockFunc(func(g *Group) {
			g.Switch(Id(em.Source().Name())).BlockFunc(func(g *Group) {
				for s, t := range em.EnumsMap() {
					returns := []Code{Qual(em.Target().Path(), t)}
					if withErr {
						returns = append(returns, Nil())
					}
					g.Case(Qual(em.Source().Path(), s)).Block(
						Return(returns...),
					)
				}
				g.Default().BlockFunc(func(g *Group) {
					def, ok := em.Default()
					switch {
					case ok && def.IsConst && withErr:
						g.Return(Qual(targetPath, def.Value), Nil())
					case ok && def.IsConst:
						g.Return(Qual(targetPath, def.Value))
					case ok && withErr:
						if def.IsString {
							g.Return(Lit(def.Value), Nil())
						} else {
							g.Return(Id(def.Value), Nil())
						}
					case ok:
						if def.IsString {
							g.Return(Lit(def.Value))
						} else {
							g.Return(Id(def.Value))
						}
					case withErr:
						if def.IsString {
							g.Return(Lit(def.Value), Qual("fmt", "Errorf").Call(Lit(msg), Id("pt")))
						} else {
							g.Return(Id(def.Value), Qual("fmt", "Errorf").Call(Lit(msg), Id("pt")))
						}
					default:
						g.Panic(Qual("fmt", "Sprintf").Call(Lit("unsupported enum value: %v"), Id(em.Source().Name())))
					}
				})
			})
		})
	f.Line()
}

func (m mapperFunc) generateSignature() {
	if !m.isRoot {
		m.generatedFn.Params(Id("src").Qual(m.source.Type().Path(), m.source.Type().TypeName()))
		m.generatedFn.Params(Qual(m.target.Type().Path(), m.target.Type().TypeName()), jen.Error())
		return
	}

	for i, p := range m.mapper.Params() {
		_, typeName := p.Type()
		pname := p.Name()
		if i == 0 {
			pname = "src"
		}
		m.generatedFn.Params(Id(pname).Qual(p.Path(), typeName))
	}

	returns := []Code{}
	for _, r := range m.mapper.Results() {
		_, typeName := r.Type()
		returns = append(returns, Qual(r.Path(), typeName))
	}

	m.generatedFn.Params(returns...)
}

func (m mapperFunc) generateBlock() error {
	var err error
	m.generatedFn.BlockFunc(func(g *Group) {
		bl := mapperBlock{
			Group:      g,
			mapperFunc: m,
		}
		var targetPath string
		var targetTypeName string
		var targetFields []mapp.Field

		if m.isRoot {
			target := m.mapper.Target()
			targetPath = target.Path()
			_, t := target.Type()
			targetTypeName = t
			targetFields = target.Fields()
		} else {
			target := m.target
			targetPath = target.Type().Path()
			targetTypeName = target.Type().TypeName()
			targetFields = target.Fields()
		}

		bl.Id("target").Op(":=").Qual(targetPath, targetTypeName+"{}")
		bl.Line()
		for _, tf := range targetFields {
			err = bl.generateTargetMapping(tf)
			if err != nil {
				return
			}
		}
		bl.Line()
		returns := []jen.Code{Id("target")}
		if !m.isRoot || m.mapper.WithError() {
			returns = append(returns, Nil())
		}

		bl.Return(returns...)
	})

	return err
}

func (bl mapperBlock) generateTargetMapping(target mapp.Field) error {
	_, ok := bl.mapper.RulesBy(target.FullName(), mapp.RuleTypeIgnore)
	if ok {
		return nil
	}

	source, ok := bl.mapper.SourceFieldByTarget(target.FullName())
	if !ok {
		return fmt.Errorf("'%s' has no source field. Use '@ignore -t=%s' or '@qual -t=%s -s=<.Path.To.The.Source>'", target.FullName(), target.FullName(), target.FullName())
	}

	genFn, ok := bl.fieldMapGenerators[source.Type().TypeFamily()][target.Type().TypeFamily()]
	if !ok {
		panic(fmt.Sprintf("unsupported mapping from: %s to %s", source.Type().TypeFamily(), target.Type().TypeFamily()))
	}

	err := genFn(bl, source, target, isTargetStrct(bl.isRoot), withErr(bl.mapper.WithError()))
	if err != nil {
		return fmt.Errorf("error generate mapping from '%s%s' to '%s%s': %w", source.Type().TypeName(), source.FullName(), target.Type().TypeName(), target.FullName(), err)
	}

	return nil
}
