func Generate(mf mapp.File) {
	f := NewFile("main")

	f.Comment("Code generated by go-mapp. DO NOT EDIT.")

	fldmap := map[mapp.TypeFamily]map[mapp.TypeFamily]fldMapFunc{
		mapp.FieldTypeBasic: {
			mapp.FieldTypeBasic:   basicToBasic,
			mapp.FieldTypePointer: basicToPointer},
		mapp.FieldTypePointer: {
			mapp.FieldTypeBasic:   pointerToBasic,
			mapp.FieldTypeStruct:  pointerToStruct,
			mapp.FieldTypePointer: pointerToPointer,
		},
		mapp.FieldTypeStruct: {
			mapp.FieldTypeStruct:  structToStruct,
			mapp.FieldTypePointer: structToPointer,
		},
		mapp.FieldTypeSlice: {mapp.FieldTypeSlice: sliceToSlice},
		mapp.FieldTypeNamed: {mapp.FieldTypeNamed: namedToNamed},
	}

	enmMappers := make(map[string]map[string]mapp.EnumMapper)

	for _, em := range mf.EnumsMappers() {
		if tm, ok := enmMappers[enumHash(em.Source())]; ok {
			if _, ok := tm[enumHash(em.Target())]; ok {
				continue
			}
		}
		generateEnumMapper(f, em, func(s, t string, em mapp.EnumMapper) { enmMappers[s] = map[string]mapp.EnumMapper{t: em} })
	}
	for _, m := range mf.Mappers() {
		opts := []genOptFunc{
			file(f),
			submappers(map[string]string{}),
			enumMappers(enmMappers),
			fldMapFuncs(fldmap),
			mapper(m),
			sourcePath(m.Source().Path()),
			sourceType(m.Source().TypeName()),
			targetPath(m.Target().Path()),
			targetType(m.Target().TypeName()),
			withErr(m.WithError()),
			// ttFields(m.Target().Fields()),
		}

		err := generateMapper(
			m.Name(),
			opts...,
		)
		if err != nil {
			log.Fatalf("Error generate mapper '%s': %v", m.Name(), err)
		}
	}

	err := f.Save("./mapper_impl.go")
	if err != nil {
		log.Fatalf("failed to save file: %v", err)
	}
}

func generateEnumMapper(f *File, em mapp.EnumMapper, include func(s, t string, em mapp.EnumMapper)) {
	sourcePath := em.Source().Path()
	targetPath := em.Target().Path()
	_, sourceT := em.Source().Type()
	_, targetT := em.Target().Type()

	include(enumHash(em.Source()), enumHash(em.Target()), em)

	msg, withErr := em.Errormsg()
	sign := f.Func().Id(em.Name()).Params(Id(em.Source().Name()).Qual(sourcePath, sourceT))
	if withErr {
		sign.Params(Qual(targetPath, targetT), Id("error"))
	} else {
		sign.Qual(targetPath, targetT)
	}
	sign.
		BlockFunc(func(g *Group) {
			g.Switch(Id(em.Source().Name())).BlockFunc(func(g *Group) {
				for s, t := range em.EnumsMap() {
					returns := []Code{Qual(em.Target().Path(), t)}
					if withErr {
						returns = append(returns, Nil())
					}
					g.Case(Qual(em.Source().Path(), s)).Block(
						Return(returns...),
					)
				}
				g.Default().BlockFunc(func(g *Group) {
					def, ok := em.Default()
					switch {
					case ok && def.IsConst && withErr:
						g.Return(Qual(targetPath, def.Value), Nil())
					case ok && def.IsConst:
						g.Return(Qual(targetPath, def.Value))
					case ok && withErr:
						if def.IsString {
							g.Return(Lit(def.Value), Nil())
						} else {
							g.Return(Id(def.Value), Nil())
						}
					case ok:
						if def.IsString {
							g.Return(Lit(def.Value))
						} else {
							g.Return(Id(def.Value))
						}
					case withErr:
						if def.IsString {
							g.Return(Lit(def.Value), Qual("fmt", "Errorf").Call(Lit(msg), Id("pt")))
						} else {
							g.Return(Id(def.Value), Qual("fmt", "Errorf").Call(Lit(msg), Id("pt")))
						}
					default:
						g.Panic(Qual("fmt", "Sprintf").Call(Lit("unsupported enum value: %v"), Id(em.Source().Name())))
					}
				})
			})
		})
	f.Line()
}

func generateMapper(mname string, opts ...genOptFunc) error {
	gp := gParams(opts...)
	stm := gp.file.Func().Id(mname).Params(Id("src").Qual(gp.srcPath, gp.srcType))
	returns := []Code{Qual(gp.ttPath, gp.ttType)}
	if gp.withErr {
		returns = append(returns, Error())
	}

	stm.Params(returns...)

	var err error
	stm.BlockFunc(func(g *Group) {
		assign(g).new("target").emptyStruct(gp.ttPath, gp.ttType)
		for _, tf := range gp.ttFields {
			err = generateTargetMapping(g, tf, opts...)
			if err != nil {
				return
			}
		}
		switch {
		case gp.withErr:
			retrn(g).Val("target").Nil()
		default:
			retrn(g).Val("target").build()
		}
	})

	return err
}

